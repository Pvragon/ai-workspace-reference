generate_manifest() {
    local manifest_file="$1"
    local workspace_root="$2"
    local username=$(whoami)
    
    cat > "$manifest_file" << EOF
# workspace-manifest.yaml
# Generated by: bootstrap_local.sh on $(date)
# Location: $manifest_file
# Purpose: Single source of truth for workspace paths across all machines

version: "1.0"
generated_at: "$(date -u +%Y-%m-%dT%H:%M:%SZ)"
workspace_layout: "multi-root"
workspace_root: $workspace_root

roots:
  personal:
    path: $workspace_root/personal
    role: user-local
    description: "Private human workspace - notes, secrets, preferences"
    is_git_repo: false
    is_gitignored: true
    key_subdirs:
      notes: notes/
      scratch: scratch/
      preferences: preferences/
      secrets: secrets/
    
  team_lib:
    path: $workspace_root/team-lib
    role: shared-sop
    description: "Shared team library - SOPs, directives, templates"
    is_git_repo: true
    github_repo: Pvragon/pvragon-ai-library
    is_gitignored: false
    key_subdirs:
      registry: registry/
      directives: directives/
      skills: skills/
      personas: personas/
      context_source: context/
      executions: executions/
      harnesses: harnesses/
      admin: _admin/
    
  my_lib:
    path: $workspace_root/my-lib
    role: user-directives
    description: "Personal overlay library - user directives & scripts"
    is_git_repo: true
    github_repo: jkhereford/private-ai-library
    is_gitignored: false
    key_subdirs:
      archive: archive/
      backlog: backlog/
      registry: registry/
      directives: directives/
      skills: skills/
      personas: personas/
      context_source: context/
      executions: executions/
      harnesses: harnesses/
      runtime: runtime/
    
  projects:
    path: $workspace_root/projects
    role: active-repos
    description: "Active development - all project repositories"
    is_git_repo: false
    contains_git_repos: true
    key_subdirs: {}

context_layers:
  - team_lib
  - my_lib
  - personal

project_structure:
  src: "Source code"
  docs: "Documentation"
  docs/specs: "Requirements and definition of done"
  docs/context: "Project context for agents"
  docs/directives: "Project-specific rules and workflows"
  README.md: "Project overview"
EOF
    echo "Created manifest: $manifest_file"
}

